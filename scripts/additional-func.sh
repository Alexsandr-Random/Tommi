# Add Daily Update Cron Job
function daily_update_cronjob(){
    clear
    echo -e "\e[34m---------------------------------------------------------------------------------------------------------\e[00m"
    echo -e "\e[93m[+]\e[00m Adding Daily System Update Cron Job"
    echo -e "\e[34m---------------------------------------------------------------------------------------------------------\e[00m"
    echo ""
    echo "Creating Daily Cron Job"
    job="@daily apt update; apt dist-upgrade -y; apt upgrade -yq"
    touch job
    echo $job >> job
    crontab job
    rm job
}

