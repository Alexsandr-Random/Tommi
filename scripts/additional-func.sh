# Create Privileged User
function admin_user(){
    clear
    echo -e "\e[34m---------------------------------------------------------------------------------------------------------\e[00m"
    echo -e "\e[93m[+]\e[00m We will now Create a New SUDO User"
    echo -e "\e[34m---------------------------------------------------------------------------------------------------------\e[00m"
    echo -n " Type the new username: "; read username
    adduser $username --shell /bin/bash
    usermod -aG sudo $username
    echo "$username ALL=(ALL) NOPASSWD: ALL" >> /etc/sudoers
}
